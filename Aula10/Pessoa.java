class Pessoa{
    private String nome;
    private boolean afetado;

    public String getNome() {
        return nome;
    }

    public boolean getAfetado() {
        return afetado;
    }

    public void setAfetado(boolean afetado) {
        this.afetado = afetado;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
}